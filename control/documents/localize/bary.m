      1,   1,   1
]

R = [ cosd( 40), -sind( 40), 0; sind( 40), cosd( 40), 0; 0, 0, 1 ];
B3 = R * [
    -20,  60,  20;
      0, -20,  60;
      1,   1,   1
]

